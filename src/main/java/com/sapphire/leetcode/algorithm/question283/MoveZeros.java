public class MoveZeros {
    public void moveZeroes(int[] nums) {
        int i = nums.length - 1;
        int j = nums.length - 1;

        while (j >= 0) {
            if (nums[j] == 0) {
                j--;
                continue;
            }


        }
    }
}
